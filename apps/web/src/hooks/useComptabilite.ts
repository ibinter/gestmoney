// ─── Clés de cache ───────────────────────────────────────────────────────────

export const COMPTA_KEYS = {
  all: ['comptabilite'] as const,
  exercices: () => ['comptabilite', 'exercices'] as const,
  chart: () => ['comptabilite', 'chart'] as const,
  journal: (p: JournalParams) => ['comptabilite', 'journal', p] as const,
  balance: (fy?: string) => ['comptabilite', 'balance', fy ?? 'all'] as const,
  resultat: (fy?: string) => ['comptabilite', 'resultat', fy ?? 'all'] as const,
  bilan: (fy?: string) => ['comptabilite', 'bilan', fy ?? 'all'] as const,
};

export interface JournalParams {
  fiscalYearId?: string;
  accountNumber?: string;
  startDate?: string;
  endDate?: string;
  page?: number;
  limit?: number;
}

/** Retire les clés vides pour ne pas envoyer de query params invalides (DTO strict côté API). */
function nettoyer(params: Record<string, unknown>): Record<string, unknown> {
  const out: Record<string, unknown> = {};
  for (const [k, v] of Object.entries(params)) {
    if (v !== undefined && v !== null && v !== '') out[k] = v;
  }
  return out;
}

// ─── Requêtes ────────────────────────────────────────────────────────────────

/** Exercices fiscaux du tenant — GET /accounting/fiscal-year */
export function useExercicesFiscaux() {
  return useQuery({
    queryKey: COMPTA_KEYS.exercices(),
    queryFn: async (): Promise<ExerciceFiscal[]> => {
      const res = await api.get('/accounting/fiscal-year');
      return Array.isArray(res.data) ? res.data : res.data?.data ?? [];
    },
  });
}

