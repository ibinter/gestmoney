// ─── Utilitaires d'affichage ─────────────────────────────────────────────────

/** Les montants arrivent en chaînes décimales ("1234.00") pour éviter les arrondis. */
export function versNombre(v: string | number | null | undefined): number {
  const n = typeof v === 'number' ? v : parseFloat(String(v ?? '0'));
  return Number.isFinite(n) ? n : 0;
}
